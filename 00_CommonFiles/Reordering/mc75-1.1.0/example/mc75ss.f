      INTEGER MAXN,MAXNZ,LA,LW,LIW
      PARAMETER( MAXN=10, MAXNZ=11)
      PARAMETER ( LA=4*MAXNZ, LIW=14*MAXN+7, LW=8*MAXN  )
      REAL  A(LA), W(LW), COND(2)
      INTEGER IRN(LA), JCN(LA), IW(LIW), INFO(5)
      INTEGER N,NZ,I,ICNTL(5)
C
C       READ SPARSE MATRIX
C
      READ(5,9300) N, NZ
      READ(5,9300) (IRN(I),I=1,NZ)
      READ(5,9300) (JCN(I),I=1,NZ)
      READ(5,9400) (A(I),I=1,NZ)
C
C       INITIALIZE
C
      CALL MC75I(ICNTL)
C
C       COMPUTE CONDITION NUMBERS
C
      CALL MC75A(N,NZ,LA,A,IRN,JCN,COND,LIW,IW,LW,W,ICNTL,INFO)
      IF (INFO(1) .GE. 0) THEN
        WRITE(6,9100) ' CLASSICAL CONDITION NUM. ', COND(1)
        WRITE(6,9100) ' SKEEL     CONDITION NUM. ', COND(2)
      ELSE
        WRITE(6,9200) ' INFO(1)     ', INFO(1)
      ENDIF
 9100 FORMAT(1X,A30,1P,E10.3)
 9200 FORMAT(1X,A10,I10)
 9300 FORMAT(11I5)
 9400 FORMAT(3E11.4)
      STOP
      END
